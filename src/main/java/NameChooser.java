public class NameChooser {

    public String getName(String persona) {
        if (persona.equals("School dropout"))
            return "Marshall Mathers";
        if (persona.equals("Rap superstar"))
            return "Eminem";
        if (persona.equals("Psycho"))
            return "Slim Shady";
        if (persona.equals("Rich drug addict"))
            return "I forgot my name! My new name is Rain Man";
        if (persona.equals("Well, that would be me"))
            return "Koba Kareli";
        return "Bond, James Bond";
    }

}
